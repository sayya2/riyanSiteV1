"use client";
import {
  ChevronDown,
  ChevronRightCircle,
  Facebook,
  Instagram,
  Twitter,
} from "lucide-react";
import { useEffect, useState } from "react";
import Link from "next/link";
import Image from "next/image";
import { usePathname } from "next/navigation";

interface MenuItem {
  id: number;
  title: string;
  url: string;
  children?: MenuItem[];
}

const menuItems: MenuItem[] = [
  { id: 1, title: "Home", url: "/" },
  { id: 2, title: "Projects", url: "/projects" },
  { id: 3, title: "News", url: "/news" },
  {
    id: 4,
    title: "Firm",
    url: "/firm",
    children: [
      { id: 41, title: "About", url: "/firm/about" },
      { id: 42, title: "Career", url: "/firm/career" },
      { id: 43, title: "Internships", url: "/firm/career/internships" },
      { id: 44, title: "Contact", url: "/firm/contact" },
    ],
  },
];

export default function Navbar() {
  const [isPanelOpen, setIsPanelOpen] = useState(false);
  const [isFirmOpen, setIsFirmOpen] = useState(false);
  const [isSticky, setIsSticky] = useState(false);
  const pathname = usePathname();
  const isHome = pathname === "/";

  useEffect(() => {
    const handleScroll = () => setIsSticky(window.scrollY > 100);
    handleScroll();
    window.addEventListener("scroll", handleScroll);
    return () => window.removeEventListener("scroll", handleScroll);
  }, []);

  useEffect(() => {
    setIsFirmOpen(false);
  }, [pathname]);

  const borderColor = isSticky ? "border-gray-200" : "border-red-700/0";
  const textColor = isSticky ? "text-gray-900" : "text-white";
  const baseBg =
    isHome && !isSticky
      ? "bg-transparent"
      : "bg-gradient-to-r from-[#7a1c1a] via-[#7a1c1a] to-[#9b2c28]";

  return (
    <header
      className={`fixed top-0 left-0 right-0 z-50 border-b ${borderColor} transition-all duration-300 ${
        isSticky ? "bg-white shadow-md" : baseBg
      }`}
    >
      <div className="container mx-auto px-12 ">
        <div className="flex items-center justify-between h-20">
          {/* Logo */}
          <div className="flex ">
            <Link href="/" className="flex items-center">
              <div className="relative w-36 h-12">
                <Image
                  src={
                    isSticky
                      ? "http://beta.riyan.com.mv/wp-content/uploads/2021/06/lg60n.png"
                      : "http://beta.riyan.com.mv/wp-content/uploads/2021/06/lg60wn.png"
                  }
                  alt="Riyan"
                  fill
                  className="object-contain"
                  priority
                />
              </div>
            </Link>
          </div>

          {/* Desktop Navigation */}
          <div
            className="hidden lg:block relative"
            onMouseLeave={() => setIsFirmOpen(false)}
          >
            <nav className="flex items-center space-x-8">
              {menuItems.map((item) =>
                item.children ? (
                  <button
                    key={item.id}
                    type="button"
                    onClick={() => setIsFirmOpen((prev) => !prev)}
                    onMouseEnter={() => setIsFirmOpen(true)}
                    onFocus={() => setIsFirmOpen(true)}
                    className={`group inline-flex items-center text-sm font-medium transition-colors ${
                      isSticky
                        ? "text-gray-900 hover:text-primary"
                        : "text-white hover:text-white/80"
                    }`}
                    aria-expanded={isFirmOpen}
                  >
                    {item.title}
                    <ChevronDown
                      className={`ml-1 h-4 w-4 transition-transform ${
                        isFirmOpen ? "rotate-180" : ""
                      }`}
                    />
                  </button>
                ) : (
                  <Link
                    key={item.id}
                    href={item.url}
                    className={`text-sm font-medium transition-colors ${
                      isSticky
                        ? "text-gray-900 hover:text-primary"
                        : "text-white hover:text-white/80"
                    }`}
                  >
                    {item.title}
                  </Link>
                )
              )}
            </nav>

            <div
              className={`absolute left-0 top-full pt-3 transition-all duration-200 ${
                isFirmOpen
                  ? "opacity-100 translate-y-0 pointer-events-auto"
                  : "opacity-0 -translate-y-2 pointer-events-none"
              }`}
              style={{ zIndex: 60 }}
            >
              <div className="w-64 rounded-xl border border-gray-200 bg-white shadow-lg overflow-hidden">
                <div className="flex flex-col divide-y divide-gray-100">
                  {menuItems
                    .find((m) => m.children)
                    ?.children?.map((child) => (
                      <Link
                        key={child.id}
                        href={child.url}
                        className="px-4 py-3 hover:bg-gray-50 transition-colors text-left"
                      >
                        <p className="text-sm font-semibold text-gray-900">{child.title}</p>
                        <p className="text-xs text-gray-600">
                          Learn more about {child.title.toLowerCase()} at Riyan.
                        </p>
                      </Link>
                    ))}
                </div>
              </div>
            </div>

          </div>

          {/* Contact Panel Toggle */}
          <button
            onClick={() => setIsPanelOpen(true)}
            className="flex flex-col items-center justify-center w-11 h-10 space-y-1.5 rounded-full border border-transparent hover:border-white/40 hover:bg-white/10 transition-colors"
            aria-label="Open contact panel"
            aria-expanded={isPanelOpen}
          >
            <span
              className={`block w-4 h-0.5 transition-all ${
                isSticky ? "bg-gray-900" : "bg-white"
              }`}
            />
            <span
              className={`block w-4 h-0.5 transition-all ${
                isSticky ? "bg-gray-900" : "bg-white"
              }`}
            />
            <span
              className={`block w-4 h-0.5 transition-all ${
                isSticky ? "bg-gray-900" : "bg-white"
              }`}
            />
          </button>
        </div>
      </div>

      {/* Contact Slide Panel */}
      <div
        className={`fixed inset-0 z-50 transition duration-300 ease-in-out ${
          isPanelOpen ? "pointer-events-auto" : "pointer-events-none"
        }`}
        aria-hidden={!isPanelOpen}
      >
        <div
          className={`absolute inset-0 bg-black/60 transition-opacity duration-300 ${
            isPanelOpen ? "opacity-100" : "opacity-0"
          }`}
          onClick={() => setIsPanelOpen(false)}
        />
        <aside
          className={`absolute inset-y-0 right-0 w-full max-w-md bg-white shadow-2xl transform transition-transform duration-300 ${
            isPanelOpen ? "translate-x-0" : "translate-x-full"
          }`}
        >
          <div className="flex items-start justify-between px-6 py-10 border-b border-gray-200">
            <div>
              <p className="text-3xl uppercase tracking-[0.35em] text-primary mb-8">
                Contact Us
              </p>
              <p className="text-md font-semibold text-gray-900">
                Great things in business are never done by one.
              </p>
            </div>
            <button
              onClick={() => setIsPanelOpen(false)}
              aria-label="Close contact panel"
              className="p-2 rounded-full text-gray-500 hover:text-primary hover:bg-gray-100 transition"
            >
              Ã—
            </button>
          </div>

          <div className="relative h-full overflow-y-auto">
            <div className="px-6 py-6 space-y-6 max-w-xl">
              <p className="text-gray-700 leading-relaxed">
                Reach out to us, let&apos;s discuss about how we can help you.
              </p>

              <div className="space-y-2">
                <h3 className="text-sm font-semibold text-gray-900 uppercase tracking-wide">
                  Need Help?
                </h3>
              </div>

              <div className="space-y-4">
                <div>
                  <p className="text-xs uppercase tracking-wide text-gray-500">
                    Email us directly
                  </p>
                  <a
                    href="mailto:info@riyan.com.mv"
                    className="text-lg font-semibold text-primary hover:underline"
                  >
                    info@riyan.com.mv
                  </a>
                </div>
                <div>
                  <p className="text-xs uppercase tracking-wide text-gray-500">
                    Call us directly
                  </p>
                  <a
                    href="tel:+9603315049"
                    className="text-lg font-semibold text-primary hover:underline"
                  >
                    +960 331 5049
                  </a>
                </div>
                <div>
                  <p className="text-xs uppercase tracking-wide text-gray-500 mb-2">
                    On Social Media
                  </p>
                  <div className="flex gap-6 flex-wrap">
                    <a
                      href="#"
                      className="text-gray-700 hover:text-primary font-medium"
                    >
                      <Facebook />
                    </a>
                    <a
                      href="#"
                      className="text-gray-700 hover:text-primary font-medium"
                    >
                      <Twitter />
                    </a>
                    <a
                      href="#"
                      className="text-gray-700 hover:text-primary font-medium"
                    >
                      <Instagram />
                    </a>
                  </div>
                </div>
              </div>

              {/* <div className="pt-4 border-t border-gray-200 space-y-2">
                <p className="text-xs uppercase tracking-wide text-gray-500">
                  Navigation
                </p>
                <div className="flex flex-col gap-2">
                  {menuItems.map((item) => (
                    <Link
                      key={item.id}
                      href={item.url}
                      className="text-gray-900 hover:text-primary font-medium"
                      onClick={() => setIsPanelOpen(false)}
                    >
                      {item.title}
                    </Link>
                  ))}
                </div>
              </div> */}
              <button className=" right-4 top-20 text-xs tracking-[0.25em] uppercase text-gray-300 h-16 w-60 bg-primary flex items-center justify-center hover:bg-primary-dark transition-colors rounded-md shadow-md">
                Get in Touch
                <ChevronRightCircle className="ml-3 h-7 w-7 animate-wiggle-right" />
              </button>
            </div>
          </div>
        </aside>
      </div>
    </header>
  );
}
